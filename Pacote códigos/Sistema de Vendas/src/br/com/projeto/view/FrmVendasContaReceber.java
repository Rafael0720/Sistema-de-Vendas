    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed

        if (botao == "novo" || botao == "editar") {
            JOptionPane.showMessageDialog(null, "Há um arquivo aberto. Cancele ou Salve ele antes de "
                    + "realizar essa operação!");
        } else {

            botao = "novo";
            btnNovo.setSelected(true);

            painel_vendas.setSelectedIndex(1);

            ContasRecDao dao = new ContasRecDao();
            int contador = dao.ultimoContador() + 1;
            txtId.setText("C-" + contador);

            txtSequencia.setText("(1-1)");
            txtStatus.setText("Rec");

            Date hoje = new Date();
            txtEmissao.setDate(hoje);
            txtVencimento.setDate(hoje);

            util.limpaTela_Apenas(pnl_cliente);

            txtOrigem.setText("Contas a Receber");
            txtIdCliente.setEditable(true);
            txtIdPlano.setEditable(true);

            util.limpaTela_Habilita(pnl_valores);

            txtValorFinal.setEditable(false);
            txtObservacoes.setEditable(true);
            txtObservacoes.setText("");
        }

    }//GEN-LAST:event_btnNovoActionPerformed


    private void txtValorVendaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtValorVendaKeyPressed

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            valorVenda = Double.parseDouble(txtValorVenda.getText().replace(".", "").replace(",", "."));
            juros = (valorVenda / 100) * jurosPor;
            desconto = (valorVenda / 100) * descontoPor;
            valorFinal = valorVenda + juros - desconto;
            txtValorFinal.setText(String.valueOf(decimal.format(valorFinal)));

            if (txtJurosPorc.getText().equals("")) {
                txtJuros.setText("");
            } else {
                txtJuros.setText(String.valueOf(decimal.format(juros)));
            }

            if (txtDescontoPorc.getText().equals("")) {
                txtDesconto.setText("");
            } else {
                txtDesconto.setText(String.valueOf(decimal.format(desconto)));
            }
            txtValorVenda.setText(String.valueOf(decimal.format(valorVenda)));

            txtJurosPorc.requestFocus();
            txtJurosPorc.selectAll();

        }

    }//GEN-LAST:event_txtValorVendaKeyPressed


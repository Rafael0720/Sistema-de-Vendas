    private void btnApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApagarActionPerformed

        String id = txtId.getText();
        String origem = txtOrigem.getText();

        if (botao == "novo" || botao == "editar") {
            JOptionPane.showMessageDialog(null, "Há um arquivo aberto. Cancele ou Salve ele antes de "
                    + "realizar essa operação!");
        } else if (id.equals("")) {
            JOptionPane.showMessageDialog(null, "Não há nenhum arquivo para ser excluído!");
        } else {
            if (origem.equals("Requisição")) {
                JOptionPane.showMessageDialog(null, "Essa conta tem uma Requisição como origem. Só pode ser cancelada "
                        + "diretamente na requisição!");
            } else {
                
                // percorre a tabela e remove a linha da conta que foi excluída.
                DefaultTableModel tbContas = (DefaultTableModel) tbContasReceber.getModel();
                for (int i = 0; i < tbContas.getRowCount(); i++) {
                    String titulo = tbContas.getValueAt(i, 0).toString();
                    if (titulo.equals(id)) {
                        tbContas.removeRow(i);
                    }
                }
                ContasRecDao dao = new ContasRecDao();
                dao.excluir(id);
                util.limpaTela_Apenas(pnl_titulo);
                util.limpaTela_Apenas(pnl_cliente);
                util.limpaTela_Apenas(pnl_valores);
                txtEmissao.setCalendar(null);
                txtVencimento.setCalendar(null);
                txtObservacoes.setText("");
            }
        }

    }//GEN-LAST:event_btnApagarActionPerformed


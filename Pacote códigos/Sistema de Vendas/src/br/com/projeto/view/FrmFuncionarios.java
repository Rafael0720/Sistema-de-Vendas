    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed

        if (botao == null) {
            JOptionPane.showMessageDialog(null, "Não há nenhum arquivo para ser cancelado!");
        } else if (botao.equals("novo")) {
            new Utilitarios().limpaTela_Desabilita(dados_pessoais);
            new Utilitarios().limpaTela_Desabilita(endereco);
            txtObservacoes.setEditable(false);
            cbCargo.setSelectedItem("Vendedor");
            cbNivel.setSelectedItem("fraco");
            cbUf.setSelectedItem("SP");
            txtObservacoes.setText("");

            btnNovo.setSelected(false);

            botao = null;
        } else if (botao.equals("editar")) {

            int id = Integer.parseInt(txtId.getText());
            FuncionariosDao dao = new FuncionariosDao();
            Funcionarios obj = new Funcionarios();
            obj = dao.consultarPorId(id);

            new Utilitarios().Desabilita_Apenas(dados_pessoais);
            new Utilitarios().Desabilita_Apenas(endereco);
            txtObservacoes.setEditable(false);

            txtId.setText(String.valueOf(obj.getId()));
            txtNome.setText(obj.getNome());
            txtCpf.setText(obj.getCpf());
            txtRg.setText(obj.getRg());

            SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
            Date nascimento;
            try {
                nascimento = formato.parse(obj.getNascimento());
                txtNascimento.setDate(nascimento);
            } catch (Exception e) {
            }

            txtTelefone.setText(obj.getTelefone());
            txtCelular.setText(obj.getCelular());
            cbCargo.setSelectedItem(obj.getCargo());
            txtEmail.setText(obj.getEmail());
            txtSenha.setText(obj.getSenha());
            cbNivel.setSelectedItem(obj.getNivel_acesso());
            txtEndereco.setText(obj.getEndereco());
            txtNumero.setText(obj.getNumero());
            txtComplemento.setText(obj.getComplemento());
            txtBairro.setText(obj.getBairro());
            txtCidade.setText(obj.getCidade());
            cbUf.setSelectedItem(obj.getUf());
            txtCep.setText(obj.getCep());
            txtObservacoes.setText(obj.getObservacoes());

            btnEditar.setSelected(false);

            botao = null;

        }

    }//GEN-LAST:event_btnCancelarActionPerformed


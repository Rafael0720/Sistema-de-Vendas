    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated

        Date hoje = new Date();
        txtPagamento.setDate(hoje);

        DefaultTableModel tabelaContas = (DefaultTableModel) tbContasPagar.getModel();

        // caso não tenho nenhuma conta na tabela, o total inicia com zero, senão ele inicial com o valor de todas as contas.
        int linhas = tabelaContas.getRowCount();
        totalPag = 0;
        if (linhas == 0) {
            lblTotalPagar.setText("0");
        } else {
            for (int i = 0; i < linhas; i++) {
                double valorLin = Double.parseDouble(tabelaContas.getValueAt(i, 6).toString().replace(".", "").replace(",", "."));
                totalPag += valorLin;
            }
            lblTotalPagar.setText(String.valueOf(decimal.format(totalPag)));
        }

    }//GEN-LAST:event_formWindowActivated

    private void btnBaixarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBaixarActionPerformed

        DefaultTableModel contasBaixar = (DefaultTableModel) tbContasPagar.getModel();
        boolean ativo;
        String plano = txtPlano.getText();
        Date pagamento = txtPagamento.getDate();

        ContasPagDao dao = new ContasPagDao();
        ContasPag obj = new ContasPag();

        if (plano.equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha o plano e a data do pagamento!");
        } else {

            /**
             * faz a checagem se a conta está marcada para ser baixada, se
             * estiver é feito a baixa, senão mantém como está e passa pra linha
             * de baixo.
             */
            for (int i = 0; i < contasBaixar.getRowCount(); i++) {
                ativo = Boolean.parseBoolean(contasBaixar.getValueAt(i, 0).toString()); //verifico se a linha da tabela está marcada
                if (ativo == true) { //se estiver marcada entra no if
                    obj.setId(contasBaixar.getValueAt(i, 1).toString());
                    obj.setSituacao("Liq");

                    Planos objPla = new Planos();
                    objPla.setId(Integer.parseInt(txtIdPlano.getText()));
                    obj.setPlano(objPla);

                    SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
                    pagamento = txtPagamento.getDate();
                    String pagamentoFor = formato.format(pagamento);
                    obj.setBaixa(pagamentoFor);

                    dao.baixarEstornar(obj);

                }

            }

            //  remove as contas baixadas da tabela.             
            for (int i = 0; i < contasBaixar.getRowCount(); i++) {
                ativo = Boolean.parseBoolean(contasBaixar.getValueAt(i, 0).toString()); //verifico se a linha da tabela está marcada
                if (ativo == true) {
                    contasBaixar.removeRow(i);
                    i = i - 1;
                }
            }

            totalPag -= total;
            lblTotalPagar.setText(String.valueOf(decimal.format(totalPag)));
            valor = 0;
            total = 0;
            txtTotal.setText("");
            JOptionPane.showMessageDialog(null, "Arquivo baixado com sucesso!");
        }


    }//GEN-LAST:event_btnBaixarActionPerformed


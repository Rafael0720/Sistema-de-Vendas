    private void tbContasPagarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbContasPagarMouseClicked

        boolean ativo;

        /**
         * seleciona as contas que serão baixadas.
         */
        if (evt.getClickCount() == 2) {
            ativo = Boolean.parseBoolean(tbContasPagar.getValueAt(tbContasPagar.getSelectedRow(), 0).toString());

            if (ativo == false) {
                tbContasPagar.setValueAt(true, tbContasPagar.getSelectedRow(), 0);
                valor = Double.parseDouble(tbContasPagar.getValueAt(tbContasPagar.getSelectedRow(), 6).toString().replace(".", "").replace(",", "."));
                total += valor;
                txtTotal.setText(String.valueOf(decimal.format(total)));
                linhas += 1;
            } else {
                tbContasPagar.setValueAt(false, tbContasPagar.getSelectedRow(), 0);
                valor = Double.parseDouble(tbContasPagar.getValueAt(tbContasPagar.getSelectedRow(), 6).toString().replace(".", "").replace(",", "."));
                total -= valor;
                txtTotal.setText(String.valueOf(decimal.format(total)));
                linhas -= 1;
            }

        }

    }//GEN-LAST:event_tbContasPagarMouseClicked


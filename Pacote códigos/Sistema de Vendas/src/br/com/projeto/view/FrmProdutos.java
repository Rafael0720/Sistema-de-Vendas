    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed

        if (botao == null) {
            JOptionPane.showMessageDialog(null, "Não há nenhum arquivo para ser cancelado!");
        } else if (botao.equals("novo")) {

            new Utilitarios().limpaTela_Desabilita(dados);
            cbTipo.setEditable(false);
            cbTipo.setSelectedItem("Peças Carro");
            txtObservacoes.setEditable(false);
            txtObservacoes.setText("");
            txtAtualizacao.setCalendar(null);

            botao = null;
            btnNovo.setSelected(false);

        } else if (botao.equals("editar")) {

            int id = Integer.parseInt(txtId.getText());

            ProdutosDao dao = new ProdutosDao();
            Produtos obj = new Produtos();
            obj = dao.consultarPorId(id);

            new Utilitarios().Desabilita_Apenas(dados);
            cbTipo.setEditable(false);
            txtObservacoes.setEditable(false);

            txtNome.setText(obj.getNome());
            cbTipo.setSelectedItem(obj.getTipo());
            txtNcm.setText(obj.getNcm());
            txtCod_barras.setText(obj.getCod_barras());
            txtQuantidade.setText(String.valueOf(obj.getQuantidade()));

            txtValorCompra.setText(String.valueOf(decimal.format(obj.getValor_compra())));
            txtMargem_lucro.setText(String.valueOf(decimal.format(obj.getMargem_lucro())));
            txtValorVenda.setText(String.valueOf(decimal.format(obj.getValor_venda())));
            txtDesconto_vista.setText(String.valueOf(decimal.format(obj.getDesconto_vista())));
            txtValor_vista.setText(String.valueOf(decimal.format(obj.getValor_vista())));
            txtObservacoes.setText(obj.getObservacoes());
            String atualizacaoS = obj.getAtualizacao();
            try {
                Date atualizacao = formato.parse(atualizacaoS);
                txtAtualizacao.setDate(atualizacao);
            } catch (ParseException ex) {

            }
            botao = null;
            btnEditar.setSelected(false);

        }

    }//GEN-LAST:event_btnCancelarActionPerformed


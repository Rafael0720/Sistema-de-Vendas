            valorCompra = txtValorCompra.getText();
            valorCompra = valorCompra.replace(".", "").replace(",", ".");
            obj.setValor_compra(Double.parseDouble(valorCompra));
            margemLucro = txtMargem_lucro.getText();
            margemLucro = margemLucro.replace(".", "").replace(",", ".");
            obj.setMargem_lucro(Double.parseDouble(margemLucro));
            valorVenda = txtValorVenda.getText();
            valorVenda = valorVenda.replace(".", "").replace(",", ".");

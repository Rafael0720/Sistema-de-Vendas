    private void txtValorCompraKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtValorCompraKeyPressed

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            String margem_lucro = txtMargem_lucro.getText();
            String valorCompraS, margemS, descontoS;
            double valorCompra, margem, valorVenda, desconto, valorVista;

            valorCompraS = txtValorCompra.getText();
            valorCompraS = valorCompraS.replace(".", "").replace(",", ".");
            valorCompra = Double.parseDouble(valorCompraS);
            txtValorCompra.setText(String.valueOf(decimal.format((valorCompra))));

            if (margem_lucro.equals("")) {

            } else {

                margemS = txtMargem_lucro.getText();
                margemS = margemS.replace(".", "").replace(",", ".");
                margem = Double.parseDouble(margemS);
                txtMargem_lucro.setText(String.valueOf(decimal.format((margem))));

                valorVenda = valorCompra + ((valorCompra / 100) * margem);
                txtValorVenda.setText(String.valueOf(decimal.format((valorVenda))));

                descontoS = txtDesconto_vista.getText();
                descontoS = descontoS.replace(".", "").replace(",", ".");
                desconto = Double.parseDouble(descontoS);
                txtDesconto_vista.setText(String.valueOf(decimal.format((desconto))));

                valorVista = valorVenda - ((valorVenda / 100) * desconto);
                txtValor_vista.setText(String.valueOf(decimal.format(valorVista)));
            }
            txtMargem_lucro.requestFocus();
            txtMargem_lucro.selectAll();
        }
    }//GEN-LAST:event_txtValorCompraKeyPressed


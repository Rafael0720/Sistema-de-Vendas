    private void txtValorCompraKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtValorCompraKeyPressed

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            valorCompra = Double.parseDouble(txtValorCompra.getText().replace(".", "").replace(",", "."));
            juros = (valorCompra / 100) * jurosPor;
            desconto = (valorCompra / 100) * descontoPor;
            valorFinal = valorCompra + juros - desconto;
            txtValorFinal.setText(String.valueOf(decimal.format(valorFinal)));

            if (txtJurosPorc.getText().equals("")) {
                txtJuros.setText("");
            } else {
                txtJuros.setText(String.valueOf(decimal.format(juros)));
            }

            if (txtDescontoPorc.getText().equals("")) {
                txtDesconto.setText("");
            } else {
                txtDesconto.setText(String.valueOf(decimal.format(desconto)));
            }
            txtValorCompra.setText(String.valueOf(decimal.format(valorCompra)));
            txtJurosPorc.requestFocus();
            txtJurosPorc.selectAll();
        }

    }//GEN-LAST:event_txtValorCompraKeyPressed

